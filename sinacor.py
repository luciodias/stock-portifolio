# --- REG EX -----
irpfRE = re.compile(r'I\.R\.R\.F.+\$\s*([\d\.]+,\d+)\s([\d\.]+,\d+)')
dayRE = re.compile(r'\s(D)')
headRE = re.compile(r'(\d{6,8})\s+\d{1,2}\s+(\d{2}/\d{2}/\d{4})\n(.+)')
personRE = re.compile(r'Cliente .*\n(\d+)\s(\D+)\s(\d{3}\.\d{3}\.\d{3}-\d{2})')
tickRE = re.compile(r'(.+?)\s{2,12}(\w+)')
checkRE = re.compile(r'1-BOVESPA .*')	#DOUBLE CHECK
tradeRE = re.compile(r'''
                    \d-\w+\s+           #Negociação 1-BOVESPA
                    (C|V)\s+            #C/V
                    (OPCAO DE COMPRA|OPCAO DE VENDA|EXERC OPC VENDA|VISTA|FRACIONARIO|TERMO) #Tipo mercado
                    \w*\s?            #Prazo
                    ([\S \.\\]+)\s      #Título
                    #(ON|PN|UNT).?\s    #ON / PN
                    #(?:\S\S\s){0,2}?
                    #([28DFBPHXYLTI]?[#]?\s)  #OBS
                    ([0-9\.]+)\s        #Quantidade
                    ([0-9\.]+,\d+)\s    #Preço
                    ([0-9\.]+,\d+)\s    #Valor Operação
                    (C|D)               #D/C
                    ''', re.X)


notas = [os.path.join(root, file) for root, dirs, files in os.walk('./pdf') for file in files if file.endswith('.pdf')]
for nota in notas:
	with pdfplumber.open(nota) as pdf:
		for idx, page in enumerate(pdf.pages):
			printProgressBar(idx, len(pdf.pages)-1,'',nota) #Barra de progresso
			dados = []
			text = page.extract_text()
			head = headRE.search(text)
			person = personRE.search(text)
			values = [parseNum(re.search(fild+r'\s([\d\.]+,\d+)\s(D|C)',text)) for fild in filds]
			irpf = irpfRE.search(text)
			irpf = irpf.group(2) if irpf else 0
			if head is not None and person is not None: 
				dados.extend(head.groups())
				dados.extend(person.groups())
				dados.extend(values)
				dados.extend([irpf, nota.split('/')[-1],page])
				dataPregao[dados[0]] = dados
				tradeLines = checkRE.findall(text)
				ntradeLines = len(tradeLines)
				for i in tradeLines:
					j = tradeRE.search(i)
					if j is not None: 
						trade = dados[:2] #
						trade.extend(j.groups())
						company = trade[4]
						tick = tickRE.search(company)
						if tick: 
							cod = nomes[nomes.index(tick.group(1))-1] if tick.group(1) in nomes else print(tick.group(1))
							num = cods[tick.group(2)] if tick.group(1) in nomes else print(tick.group(1))
						day = dayRE.search(company) 
						day = 1 if day else 0
						trade[4]=cod+num
						trade.append(day)
						dataTrades.append(trade)
					else:
						print('Erro de leitura trade',nota,page) 
						break # Erro de leitura trade
			else:
				print('Erro de leitura cabeçalho',nota,page) 
				break # Erro de leitura cabeçalho
